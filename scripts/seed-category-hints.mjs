export function inferSeedCategory(repository, fallback = null) {
  const normalized = repository.toLowerCase();
  const override = categoryOverrides[normalized];
  if (override) {
    return override;
  }

  for (const [category, hints] of Object.entries(categoryHints)) {
    if (hints.some((hint) => normalized.includes(hint))) {
      return category;
    }
  }
  return fallback;
}
